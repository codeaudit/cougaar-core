package org.cougaar.core.service;

import org.cougaar.core.component.Service;
import org.cougaar.util.Trigger;
import org.cougaar.util.TriggerRegistry;

/**
 * Scheduler API for Component use.
 *
 * @see TriggerRegistry
 */
public interface SchedulerService extends Service, TriggerRegistry {

  /**
   * Register a client trigger, returning a "schedulerTrigger"
   * for the client to use.
   * <p>
   * <em>IMPORTANT:</em><br>
   * See the notes in <tt>TriggerRegistry.register(Trigger)</tt>
   * for threading issues.
   * @param clientTrigger the trigger to run when scheduled
   * @return a "schedulerTrigger" that the caller can use to tell 
   * the scheduler that it wants to be "clientTrigger.trigger()"ed.
   */
  Trigger register(Trigger clientTrigger);

  /**
   * Release a scheduler Trigger that was generated by 
   * <tt>register(..)</tt>.
   */
  void unregister(Trigger schedulerTrigger);
}

